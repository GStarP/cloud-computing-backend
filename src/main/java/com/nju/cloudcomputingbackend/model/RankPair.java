package com.nju.cloudcomputingbackend.model;

/**
 * @author hxw
 * @des 名+值的数据模型
 */
public class RankPair {

    private String name;
    private double rank;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getRank() {
        return rank;
    }

    public void setRank(double rank) {
        this.rank = rank;
    }

}
